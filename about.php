<H3><a name="about_site"></a>关于这个站点</H3>
<h4>写信给未来</h4>
<P>　　还记得小的时候把写给20年后自己的信埋到树下吗？人类似乎越来越喜欢给未来的自己留下什么，这仅仅是一种游戏吗？心理学上认为这源于人类的自我承诺与期待。 </P>
<p>　　给未来的自己写信的，一定是位有心人。</p>
<p>　　写下对自己未来的期望。可能是十年、二十年之后，无论是事业，还是家庭、生活等各个方面希望自己能够达到什么程度。</p>
<p>　　心理学上，有一类行为是给自己设置目标，换个词来描述叫做做出承诺。给未来的自己写信就属于这种类型。与公开承诺不同，这是一种自我承诺；尽管公开承诺往往更不容易改变，但是由于隐私等各种不便公开的原因，人们便会给未来的自己写信，以对自己做出承诺。</p>
<p>　  　除了承诺，人们给未来的自己写信的另一个动机，便是评价或者总结以邮件的形式记录下此时此刻自己的经历、感受，让5年、10年，甚至是20年之后的自己  来查看。由于人在不同的年龄阶段，认知领悟能力不同，给未来的自己写信，实则是让自己以一种更加成熟的心态来面对和思考问题。</p>
<p>　　收到邮件的时候，也许连写信人自己都已经把这件事情淡忘了。但是在一阵惊喜之后，写信人会自问，这些年，我都做了些什么？预期的目标是否达到了？哪些事情没有完成？有没有从曾经的经验中获益？</p>
<p>　　人们在写日记、周记，包括时下流行的BLOG，都是对即时事件进行的记录。人们从情感上，往往把日记人格化，将其当作一个朋友。这时，日记不再是单纯的信息的载体，它同时也是自我放松、自我宣泄的载体，它成为了人们的倾诉对象。</p>
<p>　　而给自己写的信不是出于倾诉的考虑，哪怕内容上可能会与日记近似。更多的时候，写信人会因此产生一种心理期待，从而促使自己朝着这一目标方向努力。这正是自我承诺的实现。</p>
